const execFileAsync = promisify(execFile);
const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const appRoot = path.join(repoRoot, "app");
const publicRoot = path.join(appRoot, "public");
const aggregatePath = path.join(repoRoot, "THIRD_PARTY_NOTICES.txt");
const retainedLicenseRoot = path.join(repoRoot, "third_party_licenses");
const rustReportName = "rust-dependencies.json";
const rustReportPath = path.join(retainedLicenseRoot, rustReportName);
const cargoLockPath = path.join(repoRoot, "Cargo.lock");
const aboutConfigPath = path.join(repoRoot, "about.toml");
const rustReportGenerator = "cargo-about@0.9.1";
const generatedHeader = [
  "VIGLA THIRD-PARTY NOTICES",
  "",
  "Generated by scripts/generate-license-notices.mjs from the locked production",
  "JavaScript and Rust dependency graphs and the repository's curated notices.",
  "Do not edit this file directly; run `pnpm -C app licenses:write`.",
].join("\n");
export const legalDistributionFiles = Object.freeze([
  "LICENSE",
  "NOTICE",
  "THIRD_PARTY_NOTICES.md",
  "THIRD_PARTY_NOTICES.txt",
]);
const licenseFilePattern =
  /^(?:licen[cs]e|copying|notice|copyright|patents?|third[-_ ]?party)(?:$|[._-])/i;
const sourceCodeExtensionPattern =
  /\.(?:c|cc|cpp|cxx|go|h|hh|hpp|hxx|java|js|jsx|m|mm|py|rs|swift|ts|tsx)$/i;
const licenseTemplatePlaceholderPattern =
  /<(?:year|owner|copyright holders)>/i;

function normalizedText(value) {
  return `${value.replace(/\r\n?/g, "\n").trimEnd()}\n`;
}

function packageKey(packageRecord) {
  return `${packageRecord.name}@${packageRecord.version}`;
}

function compareText(left, right) {
  return left < right ? -1 : left > right ? 1 : 0;
}

function sha256(value) {
  return createHash("sha256").update(value).digest("hex");
}

function attributionKey(packageName, licenseId) {
  return `${packageName}\0${licenseId}`;
}

function licenseTermsWithoutPlaceholders(value) {
  const normalized = normalizedText(value);
  if (!licenseTemplatePlaceholderPattern.test(normalized)) return normalized;
  return normalizedText(
    normalized
      .split("\n")
      .filter((line) => !licenseTemplatePlaceholderPattern.test(line))
      .join("\n"),
  );
}

function rustPackageKey(packageRecord) {
  return `${packageRecord.name}@${packageRecord.version}`;
}

function requireString(value, field) {
  if (typeof value !== "string" || value.length === 0) {
    throw new Error(`cargo-about report has an invalid ${field}`);
  }
  return value;
}

function collectBundledLegalFiles(packageRecord) {
  const packageRoot = path.dirname(
    requireString(packageRecord.manifest_path, "crate manifest path"),
  );
  const files = [];

  function visit(directory) {
    for (const entry of readdirSync(directory, { withFileTypes: true })) {
      const filePath = path.join(directory, entry.name);
      if (entry.isDirectory()) {
        visit(filePath);
        continue;
      }
      if (
        !entry.isFile() ||
        !licenseFilePattern.test(entry.name) ||
        sourceCodeExtensionPattern.test(entry.name)
      ) {
        continue;
      }
      const relativePath = path
        .relative(packageRoot, filePath)
        .split(path.sep)
        .join("/");
      const key = rustPackageKey(packageRecord);
      const retainedAttribution = retainedArchiveAttributions.find(
        (candidate) =>
          candidate.package === key && candidate.sourceFile === relativePath,
      );
      const contents = readFileSync(filePath);
      if (
        retainedAttribution &&
        sha256(contents) !== retainedAttribution.sha256
      ) {
        throw new Error(
          `${key} ${relativePath} does not match its pinned SHA-256`,
        );
      }
      files.push({
        package: key,
        source_file: relativePath,
        attribution_license_ids: retainedAttribution
          ? [...retainedAttribution.licenseIds]
          : [],
        ...(retainedAttribution
          ? {
              sha256: retainedAttribution.sha256,
              provenance: "checksum-pinned crate archive legal file",
            }
          : {}),
        text: normalizedText(contents.toString("utf8")),
      });
    }
  }

  visit(packageRoot);
  return files;
}

function packageVcsCommit(packageRecord) {
  const packageRoot = path.dirname(
    requireString(packageRecord.manifest_path, "crate manifest path"),
  );
  const vcsInfo = JSON.parse(
    readFileSync(path.join(packageRoot, ".cargo_vcs_info.json"), "utf8"),
  );
  return requireString(vcsInfo?.git?.sha1, "crate VCS commit");
}

function collectPinnedUpstreamLegalFiles(rawReport) {
  const packageKeys = new Set(
    rawReport.crates
      .filter((crateRecord) => typeof crateRecord?.package?.source === "string")
      .map((crateRecord) => rustPackageKey(crateRecord.package)),
  );
  const notices = [];
  for (const source of retainedRemoteLicenses) {
    if (!Array.isArray(source.packages)) continue;
    const contents = readFileSync(path.join(retainedLicenseRoot, source.fileName));
    if (sha256(contents) !== source.sha256) {
      throw new Error(`${source.fileName} does not match its pinned SHA-256`);
    }
    for (const packageName of source.packages) {
      if (!packageKeys.has(packageName)) {
        throw new Error(
          `${source.fileName} references unknown Rust package ${packageName}`,
        );
      }
      notices.push({
        package: packageName,
        source_file: `third_party_licenses/${source.fileName}`,
        attribution_license_ids: Array.isArray(source.licenseIds)
          ? [...source.licenseIds]
          : [],
        source_url: source.url,
        sha256: source.sha256,
        provenance: "checksum-pinned upstream legal file",
        text: normalizedText(contents.toString("utf8")),
      });
    }
  }
  return notices;
}

function collectMetadataAttributions(rawReport) {
  const packages = new Map(
    rawReport.crates
      .filter((crateRecord) => typeof crateRecord?.package?.source === "string")
      .map((crateRecord) => [
        rustPackageKey(crateRecord.package),
        crateRecord.package,
      ]),
  );
  return retainedMetadataAttributions.map((expected) => {
    const packageRecord = packages.get(expected.package);
    if (!packageRecord) {
      throw new Error(`metadata attribution references unknown crate ${expected.package}`);
    }
    const authors = Array.isArray(packageRecord.authors)
      ? packageRecord.authors
      : [];
    if (
      packageRecord.license !== expected.declaredLicense ||
      packageRecord.repository !== expected.repository ||
      JSON.stringify(authors) !== JSON.stringify(expected.authors) ||
      packageVcsCommit(packageRecord) !== expected.vcsCommit
    ) {
      throw new Error(`${expected.package} metadata attribution is stale`);
    }
    return {
      package: expected.package,
      attribution_license_ids: [...expected.licenseIds],
      declared_license: expected.declaredLicense,
      authors,
      repository: expected.repository,
      vcs_commit: expected.vcsCommit,
      limitation: expected.limitation,
    };
  });
}

function detectNativeComponents(rawReport) {
  const components = [];
  for (const crateRecord of rawReport.crates) {
    const packageRecord = crateRecord?.package;
    if (
      packageRecord?.name !== "ort-sys" ||
      typeof packageRecord.source !== "string"
    ) {
      continue;
    }
    const crateRoot = path.dirname(
      requireString(packageRecord.manifest_path, "ort-sys manifest path"),
    );
    const distributionTable = readFileSync(
      path.join(crateRoot, "build", "download", "dist.txt"),
      "utf8",
    );
    const versions = new Set(
      [...distributionTable.matchAll(/\/ms@([^/]+)\//g)].map((match) => match[1]),
    );
    if (versions.size !== 1) {
      throw new Error(
        `ort-sys@${packageRecord.version} references an ambiguous ONNX Runtime version`,
      );
    }
    const [version] = versions;
    const legalFiles = retainedRemoteLicenses
      .filter(
        (source) =>
          source.component === "ONNX Runtime" && source.version === version,
      )
      .map((source) => ({
        file_name: source.fileName,
        source_url: source.url,
        sha256: source.sha256,
      }))
      .sort((left, right) => compareText(left.file_name, right.file_name));
    if (legalFiles.length < 2) {
      throw new Error(
        `ONNX Runtime ${version} is missing its retained license or third-party notices`,
      );
    }
    components.push({
      name: "ONNX Runtime",
      version,
      linked_by: `ort-sys@${packageRecord.version}`,
      legal_files: legalFiles,
    });
  }
  return components.sort((left, right) =>
    compareText(`${left.name}@${left.version}`, `${right.name}@${right.version}`),
  );
}

export function canonicalizeRustReport(rawReport, cargoLock, aboutConfig) {
  if (!Array.isArray(rawReport?.crates) || !Array.isArray(rawReport?.licenses)) {
    throw new Error("cargo-about report is missing crates or licenses");
  }

  const selectedByPackage = new Map();
  const placeholderRequirements = new Set();
  const licenses = rawReport.licenses
    .map((license) => {
      const id = requireString(license.id, "license id");
      const packages = [
        ...new Set(
          (license.used_by ?? [])
            .filter((usedBy) => typeof usedBy?.crate?.source === "string")
            .map((usedBy) => {
              const packageRecord = {
                name: requireString(usedBy.crate.name, "crate name"),
                version: requireString(usedBy.crate.version, "crate version"),
              };
              const key = rustPackageKey(packageRecord);
              const selected = selectedByPackage.get(key) ?? new Set();
              selected.add(id);
              selectedByPackage.set(key, selected);
              return key;
            }),
        ),
      ].sort(compareText);
      if (packages.length === 0) return null;
      const rawText = requireString(license.text, "license text");
      const templatePlaceholdersRemoved =
        licenseTemplatePlaceholderPattern.test(rawText);
      if (templatePlaceholdersRemoved) {
        for (const packageName of packages) {
          placeholderRequirements.add(attributionKey(packageName, id));
        }
      }

      return {
        id,
        name: requireString(license.name, "license name"),
        source_file:
          typeof license.source_path === "string"
            ? path.basename(license.source_path)
            : "canonical SPDX text",
        packages,
        template_placeholders_removed: templatePlaceholdersRemoved,
        text: licenseTermsWithoutPlaceholders(rawText),
      };
    })
    .filter(Boolean)
    .sort(
      (left, right) =>
        compareText(left.id, right.id) ||
        compareText(left.packages.join("\n"), right.packages.join("\n")) ||
        compareText(left.source_file, right.source_file) ||
        compareText(left.text, right.text),
    );

  const packages = rawReport.crates
    .filter((crateRecord) => typeof crateRecord?.package?.source === "string")
    .map((crateRecord) => {
      const packageRecord = {
        name: requireString(crateRecord.package.name, "crate name"),
        version: requireString(crateRecord.package.version, "crate version"),
        source: requireString(crateRecord.package.source, "crate source"),
        source_url:
          `https://crates.io/crates/${encodeURIComponent(crateRecord.package.name)}/` +
          encodeURIComponent(crateRecord.package.version),
        declared_license: requireString(
          crateRecord.package.license,
          "declared crate license",
        ),
      };
      const selected = selectedByPackage.get(rustPackageKey(packageRecord));
      if (!selected || selected.size === 0) {
        throw new Error(
          `${rustPackageKey(packageRecord)} has no selected license text`,
        );
      }
      return {
        ...packageRecord,
        selected_licenses: [...selected].sort(compareText),
      };
    })
    .sort((left, right) =>
      compareText(rustPackageKey(left), rustPackageKey(right)),
    );

  const notices = [
    ...rawReport.crates
      .filter((crateRecord) => typeof crateRecord?.package?.source === "string")
      .flatMap((crateRecord) => collectBundledLegalFiles(crateRecord.package)),
    ...collectPinnedUpstreamLegalFiles(rawReport),
  ]
    .sort(
      (left, right) =>
        compareText(left.package, right.package) ||
        compareText(left.source_file, right.source_file) ||
        compareText(left.text, right.text),
    );
  const metadataAttributions = collectMetadataAttributions(rawReport);
  const nativeComponents = detectNativeComponents(rawReport);

  if (packages.length === 0 || licenses.length === 0) {
    throw new Error("cargo-about report contains no external dependencies");
  }
  const packageKeys = packages.map(rustPackageKey);
  if (new Set(packageKeys).size !== packageKeys.length) {
    throw new Error("cargo-about report contains duplicate package identities");
  }
  const selectedLicensesByPackage = new Map(
    packages.map((packageRecord) => [
      rustPackageKey(packageRecord),
      new Set(packageRecord.selected_licenses),
    ]),
  );
  const attributionRecords = [...notices, ...metadataAttributions];
  for (const record of attributionRecords) {
    if (
      !Array.isArray(record.attribution_license_ids) ||
      new Set(record.attribution_license_ids).size !==
        record.attribution_license_ids.length ||
      record.attribution_license_ids.some(
        (id) =>
          typeof id !== "string" ||
          !selectedLicensesByPackage.get(record.package)?.has(id),
      )
    ) {
      throw new Error(`${record.package} has an invalid attribution-license mapping`);
    }
  }
  const attributionCoverage = new Set(
    attributionRecords.flatMap((record) =>
      record.attribution_license_ids.map((id) =>
        attributionKey(record.package, id),
      ),
    ),
  );
  for (const requirement of placeholderRequirements) {
    if (!attributionCoverage.has(requirement)) {
      const [packageName, licenseId] = requirement.split("\0");
      throw new Error(
        `${packageName} ${licenseId} uses generic license terms without an exact attribution source`,
      );
    }
  }

  const report = {
    schema: 1,
    generator: rustReportGenerator,
    cargo_lock_sha256: sha256(cargoLock),
    about_config_sha256: sha256(aboutConfig),
    packages,
    licenses,
    notices,
    metadata_attributions: metadataAttributions,
    native_components: nativeComponents,
  };
  const serialized = `${JSON.stringify(report, null, 2)}\n`;
  if (serialized.includes(repoRoot) || /(?:^|["\s])\/Users\//m.test(serialized)) {
    throw new Error("canonical Rust license report contains a local path");
  }
  if (
    licenses.some((license) =>
      licenseTemplatePlaceholderPattern.test(license.text),
    )
  ) {
    throw new Error("canonical Rust license terms contain an SPDX placeholder");
  }
  return serialized;
}

async function readRustReport() {
  const [reportText, cargoLock, aboutConfig] = await Promise.all([
    readFile(rustReportPath, "utf8"),
    readFile(cargoLockPath, "utf8"),
    readFile(aboutConfigPath, "utf8"),
    verifyRetainedRemoteLicenses(),
  ]);
  const report = JSON.parse(reportText);
  if (
    report.schema !== 1 ||
    report.generator !== rustReportGenerator ||
    report.cargo_lock_sha256 !== sha256(cargoLock) ||
    report.about_config_sha256 !== sha256(aboutConfig) ||
    !Array.isArray(report.packages) ||
    !Array.isArray(report.licenses) ||
    !Array.isArray(report.notices) ||
    !Array.isArray(report.metadata_attributions) ||
    !Array.isArray(report.native_components)
  ) {
    throw new Error(
      `${path.relative(repoRoot, rustReportPath)} is stale; regenerate it with cargo-about`,
    );
  }
  if (`${JSON.stringify(report, null, 2)}\n` !== reportText) {
    throw new Error(`${path.relative(repoRoot, rustReportPath)} is not canonical`);
  }
  if (reportText.includes(repoRoot) || /(?:^|["\s])\/Users\//m.test(reportText)) {
    throw new Error("Rust license report contains a local path");
  }

  const packageKeys = new Set(report.packages.map(rustPackageKey));
  if (packageKeys.size !== report.packages.length || packageKeys.size === 0) {
    throw new Error("Rust license report has duplicate or missing packages");
  }
  for (const packageRecord of report.packages) {
    requireString(packageRecord.source, "retained crate source");
    requireString(packageRecord.source_url, "retained crate source URL");
    if (!packageRecord.source_url.startsWith("https://crates.io/crates/")) {
      throw new Error(`${rustPackageKey(packageRecord)} has an unsafe source URL`);
    }
  }
  for (const component of report.native_components) {
    requireString(component.name, "native component name");
    requireString(component.version, "native component version");
    requireString(component.linked_by, "native component linker");
    if (!packageKeys.has(component.linked_by)) {
      throw new Error(
        `${component.name} is linked by unknown crate ${component.linked_by}`,
      );
    }
    const expected = retainedRemoteLicenses
      .filter(
        (source) =>
          source.component === component.name &&
          source.version === component.version,
      )
      .map((source) => ({
        file_name: source.fileName,
        source_url: source.url,
        sha256: source.sha256,
      }))
      .sort((left, right) => compareText(left.file_name, right.file_name));
    if (JSON.stringify(component.legal_files) !== JSON.stringify(expected)) {
      throw new Error(`${component.name} has a stale retained legal-file mapping`);
    }
  }
  for (const packageRecord of report.packages) {
    const key = rustPackageKey(packageRecord);
    if (
      packageRecord.name === "ort-sys" &&
      !report.native_components.some((component) => component.linked_by === key)
    ) {
      throw new Error(`${key} is missing its linked native-component inventory`);
    }
  }
  const covered = new Map(
    report.packages.map((packageRecord) => [rustPackageKey(packageRecord), new Set()]),
  );
  for (const license of report.licenses) {
    if (licenseTemplatePlaceholderPattern.test(license.text)) {
      throw new Error(`${license.id} contains an SPDX template placeholder`);
    }
    requireString(license.id, "retained license id");
    requireString(license.name, "retained license name");
    requireString(license.source_file, "retained license source");
    requireString(license.text, "retained license text");
    if (!Array.isArray(license.packages) || license.packages.length === 0) {
      throw new Error(`${license.id} has no associated Rust packages`);
    }
    for (const key of license.packages) {
      const selected = covered.get(key);
      if (!selected) throw new Error(`${license.id} references unknown crate ${key}`);
      selected.add(license.id);
    }
  }
  for (const notice of report.notices) {
    if (!packageKeys.has(notice.package)) {
      throw new Error(`retained legal file references unknown crate ${notice.package}`);
    }
    requireString(notice.source_file, "retained legal-file source");
    requireString(notice.text, "retained legal-file text");
    if (path.isAbsolute(notice.source_file) || notice.source_file.includes("..")) {
      throw new Error(`${notice.package} has an unsafe retained legal-file path`);
    }
    if (
      notice.source_url !== undefined &&
      (typeof notice.source_url !== "string" ||
        !notice.source_url.startsWith("https://raw.githubusercontent.com/"))
    ) {
      throw new Error(`${notice.package} has an unsafe retained legal-file URL`);
    }
    if (
      !Array.isArray(notice.attribution_license_ids) ||
      new Set(notice.attribution_license_ids).size !==
        notice.attribution_license_ids.length ||
      notice.attribution_license_ids.some(
        (id) => typeof id !== "string" || !covered.get(notice.package)?.has(id),
      )
    ) {
      throw new Error(`${notice.package} has an invalid attribution-license mapping`);
    }
  }
  for (const source of retainedRemoteLicenses) {
    if (!Array.isArray(source.packages)) continue;
    for (const packageName of source.packages) {
      const notice = report.notices.find(
        (candidate) =>
          candidate.package === packageName &&
          candidate.source_file === `third_party_licenses/${source.fileName}`,
      );
      if (
        notice?.source_url !== source.url ||
        notice?.sha256 !== source.sha256 ||
        notice?.provenance !== "checksum-pinned upstream legal file" ||
        JSON.stringify(notice?.attribution_license_ids) !==
          JSON.stringify(Array.isArray(source.licenseIds) ? source.licenseIds : [])
      ) {
        throw new Error(`${packageName} has a stale pinned legal-file mapping`);
      }
    }
  }
  for (const source of retainedArchiveAttributions) {
    const notice = report.notices.find(
      (candidate) =>
        candidate.package === source.package &&
        candidate.source_file === source.sourceFile,
    );
    if (
      notice?.sha256 !== source.sha256 ||
      notice?.provenance !== "checksum-pinned crate archive legal file" ||
      JSON.stringify(notice?.attribution_license_ids) !==
        JSON.stringify(source.licenseIds)
    ) {
      throw new Error(`${source.package} has a stale crate-archive attribution mapping`);
    }
  }
  const expectedMetadataAttributions = retainedMetadataAttributions.map(
    (attribution) => ({
      package: attribution.package,
      attribution_license_ids: [...attribution.licenseIds],
      declared_license: attribution.declaredLicense,
      authors: [...attribution.authors],
      repository: attribution.repository,
      vcs_commit: attribution.vcsCommit,
      limitation: attribution.limitation,
    }),
  );
  if (
    JSON.stringify(report.metadata_attributions) !==
    JSON.stringify(expectedMetadataAttributions)
  ) {
    throw new Error("Rust dependency metadata attributions are stale");
  }
  for (const attribution of report.metadata_attributions) {
    if (
      !Array.isArray(attribution.attribution_license_ids) ||
      new Set(attribution.attribution_license_ids).size !==
        attribution.attribution_license_ids.length ||
      attribution.attribution_license_ids.some(
        (id) =>
          typeof id !== "string" || !covered.get(attribution.package)?.has(id),
      )
    ) {
      throw new Error(
        `${attribution.package} has an invalid metadata attribution-license mapping`,
      );
    }
  }
  const attributionCoverage = new Set(
    [...report.notices, ...report.metadata_attributions].flatMap((record) =>
      record.attribution_license_ids.map((id) =>
        attributionKey(record.package, id),
      ),
    ),
  );
  for (const license of report.licenses) {
    if (typeof license.template_placeholders_removed !== "boolean") {
      throw new Error(`${license.id} lacks template-attribution provenance`);
    }
    if (
      license.template_placeholders_removed &&
      license.packages.some(
        (packageName) =>
          !attributionCoverage.has(attributionKey(packageName, license.id)),
      )
    ) {
      throw new Error(`${license.id} has an unattributed SPDX template package`);
    }
  }
  for (const packageRecord of report.packages) {
    const key = rustPackageKey(packageRecord);
    if (
      !Array.isArray(packageRecord.selected_licenses) ||
      packageRecord.selected_licenses.length === 0 ||
      packageRecord.selected_licenses.some((id) => !covered.get(key).has(id))
    ) {
      throw new Error(`${key} has an incomplete selected-license mapping`);
    }
  }

  return report;
}

async function readProductionPackages() {
  const pnpm = process.platform === "win32" ? "pnpm.cmd" : "pnpm";
  const { stdout } = await execFileAsync(
    pnpm,
    ["-C", appRoot, "licenses", "list", "--prod", "--json"],
    { cwd: repoRoot, encoding: "utf8", maxBuffer: 16 * 1024 * 1024 },
  );
  const report = JSON.parse(stdout);
  if (report.error) {
    throw new Error(
      `pnpm could not enumerate production licenses: ${report.error.message}`,
    );
  }

  const packages = new Map();
  for (const entries of Object.values(report)) {
    if (!Array.isArray(entries)) continue;
    for (const entry of entries) {
      for (const packagePath of entry.paths ?? []) {
        const manifest = JSON.parse(
          await readFile(path.join(packagePath, "package.json"), "utf8"),
        );
        if (
          typeof manifest.name !== "string" ||
          typeof manifest.version !== "string" ||
          typeof manifest.license !== "string"
        ) {
          throw new Error(
            `production package at ${packagePath} lacks name, version, or license metadata`,
          );
        }

        const licenseFiles = (await readdir(packagePath, { withFileTypes: true }))
          .filter(
            (candidate) =>
              candidate.isFile() && licenseFilePattern.test(candidate.name),
          )
          .map((candidate) => candidate.name)
          .sort(compareText);
        if (licenseFiles.length === 0) {
          throw new Error(
            `${manifest.name}@${manifest.version} has no retained license or notice file`,
          );
        }

        const record = {
          name: manifest.name,
          version: manifest.version,
          license: manifest.license,
          files: await Promise.all(
            licenseFiles.map(async (name) => ({
              name,
              text: normalizedText(
                await readFile(path.join(packagePath, name), "utf8"),
              ),
            })),
          ),
        };
        const key = packageKey(record);
        const previous = packages.get(key);
        if (previous && JSON.stringify(previous) !== JSON.stringify(record)) {
          throw new Error(`${key} resolves to inconsistent license texts`);
        }
        packages.set(key, record);
      }
    }
  }

  return [...packages.values()].sort((left, right) =>
    compareText(packageKey(left), packageKey(right)),
  );
}

function addLicenseText(blocks, text, source) {
  const normalized = normalizedText(text);
  const sources = blocks.get(normalized) ?? new Set();
  sources.add(source);
  blocks.set(normalized, sources);
}

export async function generateLicenseNotices() {
  const [curatedNotice, retainedFiles, packages, rustReport] = await Promise.all([
    readFile(path.join(repoRoot, "THIRD_PARTY_NOTICES.md"), "utf8"),
    readdir(retainedLicenseRoot, { withFileTypes: true }),
    readProductionPackages(),
    readRustReport(),
  ]);
  const retainedNames = retainedFiles
    .filter((entry) => entry.isFile() && entry.name !== rustReportName)
    .map((entry) => entry.name)
    .sort(compareText);
  if (retainedNames.length === 0) {
    throw new Error("third_party_licenses contains no retained license texts");
  }

  const blocks = new Map();
  for (const name of retainedNames) {
    addLicenseText(
      blocks,
      await readFile(path.join(retainedLicenseRoot, name), "utf8"),
      `retained source: third_party_licenses/${name}`,
    );
  }
  for (const packageRecord of packages) {
    for (const licenseFile of packageRecord.files) {
      addLicenseText(
        blocks,
        licenseFile.text,
        `${packageRecord.name} ${packageRecord.version} ` +
          `(declared license: ${packageRecord.license}; source: ${licenseFile.name})`,
      );
    }
  }
  for (const license of rustReport.licenses) {
    for (const key of license.packages) {
      const packageRecord = rustReport.packages.find(
        (candidate) => rustPackageKey(candidate) === key,
      );
      if (!packageRecord) {
        throw new Error(`${license.id} references unknown crate ${key}`);
      }
      addLicenseText(
        blocks,
        license.text,
        `${packageRecord.name} ${packageRecord.version} ` +
          `(declared license: ${packageRecord.declared_license}; ` +
          `selected license: ${license.id}; source: ${license.source_file})`,
      );
    }
  }
  for (const notice of rustReport.notices) {
    const packageRecord = rustReport.packages.find(
      (candidate) => rustPackageKey(candidate) === notice.package,
    );
    if (!packageRecord) {
      throw new Error(
        `retained legal file references unknown crate ${notice.package}`,
      );
    }
    addLicenseText(
      blocks,
      notice.text,
      `${packageRecord.name} ${packageRecord.version} ` +
        (notice.provenance
          ? `(${notice.provenance}: ${notice.source_file}; ` +
            `${notice.source_url ? `source: ${notice.source_url}; ` : ""}` +
            `SHA-256: ${notice.sha256})`
          : `(retained bundled legal file: ${notice.source_file})`),
    );
  }

  const separator = "=".repeat(79);
  const javascriptInventory = packages
    .map(
      (packageRecord) =>
        `- ${packageRecord.name} ${packageRecord.version} — ` +
        `Declared license: ${packageRecord.license}`,
    )
    .join("\n");
  const rustInventory = rustReport.packages
    .map(
      (packageRecord) =>
        `- ${packageRecord.name} ${packageRecord.version} — ` +
        `Declared license: ${packageRecord.declared_license}; ` +
        `Selected: ${packageRecord.selected_licenses.join(", ")}; ` +
        `Source: ${packageRecord.source_url}`,
    )
    .join("\n");
  const nativeInventory = rustReport.native_components
    .map(
      (component) =>
        `- ${component.name} ${component.version} — ` +
        `Linked by ${component.linked_by}; retained legal files: ` +
        component.legal_files
          .map(
            (legalFile) =>
              `third_party_licenses/${legalFile.file_name} ` +
              `(SHA-256 ${legalFile.sha256}; source: ${legalFile.source_url})`,
          )
          .join(", "),
    )
    .join("\n");
  const metadataAttributionInventory = rustReport.metadata_attributions
    .map(
      (attribution) =>
        `- ${attribution.package} — Declared license: ` +
        `${attribution.declared_license}; package authors: ` +
        `${attribution.authors.join(", ") || "not supplied"}; repository: ` +
        `${attribution.repository}; source revision: ${attribution.vcs_commit}. ` +
        attribution.limitation,
    )
    .join("\n");
  const licenseSections = [...blocks.entries()]
    .map(([text, sources]) => ({
      text,
      sources: [...sources].sort(compareText),
    }))
    .sort((left, right) =>
      compareText(left.sources.join("\n"), right.sources.join("\n")),
    )
    .map(
      ({ text, sources }, index) => `${separator}
LICENSE OR NOTICE TEXT ${index + 1}

Applies to:
${sources.map((source) => `- ${source}`).join("\n")}
${separator}

${text.trimEnd()}`,
    )
    .join("\n\n");

  const text = normalizedText(`${generatedHeader}

${separator}
CURATED ATTRIBUTIONS
${separator}

${curatedNotice.trim()}

${separator}
LOCKED PRODUCTION JAVASCRIPT DEPENDENCY INVENTORY
${separator}

${javascriptInventory}

${separator}
LOCKED PRODUCTION RUST DEPENDENCY INVENTORY
${separator}

Generated with ${rustReport.generator}; Cargo.lock SHA-256:
${rustReport.cargo_lock_sha256}
about.toml SHA-256: ${rustReport.about_config_sha256}

${rustInventory}

${separator}
STATIC NATIVE COMPONENTS ENABLED BY SHIPPING FEATURES
${separator}

${nativeInventory || "- None detected"}

${separator}
UPSTREAM METADATA ATTRIBUTIONS FOR INCOMPLETE CRATE ARCHIVES
${separator}

These entries reproduce upstream package metadata without inventing a
copyright owner where upstream did not publish one.

${metadataAttributionInventory || "- None required"}

${licenseSections}`);
  if (text.includes(repoRoot)) {
    throw new Error("generated notice contains an absolute repository path");
  }

  return {
    packages,
    rustLicenses: rustReport.licenses,
    rustMetadataAttributions: rustReport.metadata_attributions,
    nativeComponents: rustReport.native_components,
    rustNotices: rustReport.notices,
    rustPackages: rustReport.packages,
    text,
  };
}

async function writeIfChanged(filePath, expected) {
  let current;
  try {
    current = await readFile(filePath, "utf8");
  } catch (error) {
    if (error?.code !== "ENOENT") throw error;
  }
  if (current !== expected) {
    await writeFile(filePath, expected, "utf8");
  }
}

async function expectedPublicFiles(aggregate) {
  const files = new Map();
  for (const name of legalDistributionFiles) {
    files.set(
      name,
      name === "THIRD_PARTY_NOTICES.txt"
        ? aggregate
        : await readFile(path.join(repoRoot, name), "utf8"),
    );
  }
  return files;
}

export async function copyLegalDistribution(destinationRoot) {
  await mkdir(destinationRoot, { recursive: true });
  await Promise.all([
    ...legalDistributionFiles.map((name) =>
      cp(path.join(repoRoot, name), path.join(destinationRoot, name)),
    ),
    cp(
      retainedLicenseRoot,
      path.join(destinationRoot, "third_party_licenses"),
      { recursive: true },
    ),
  ]);
}

async function syncPublicFiles(aggregate) {
  const publicFiles = await expectedPublicFiles(aggregate);
  await mkdir(path.join(publicRoot, "third_party_licenses"), { recursive: true });
  await Promise.all(
    [...publicFiles].map(([name, text]) =>
      writeIfChanged(path.join(publicRoot, name), text),
    ),
  );

  const sourceNames = (await readdir(retainedLicenseRoot)).sort(compareText);
  const destinationRoot = path.join(publicRoot, "third_party_licenses");
  const destinationNames = await readdir(destinationRoot);
  await Promise.all(
    destinationNames
      .filter((name) => !sourceNames.includes(name))
      .map((name) => rm(path.join(destinationRoot, name), { recursive: true })),
  );
  await Promise.all(
    sourceNames.map(async (name) =>
      writeIfChanged(
        path.join(destinationRoot, name),
        await readFile(path.join(retainedLicenseRoot, name), "utf8"),
      ),
    ),
  );
}

async function assertCurrent(filePath, expected) {
  let actual;
  try {
    actual = await readFile(filePath, "utf8");
  } catch (error) {
    if (error?.code === "ENOENT") {
      throw new Error(
        `${path.relative(repoRoot, filePath)} is missing; run ` +
          "`pnpm -C app licenses:write`",
      );
    }
    throw error;
  }
  if (actual !== expected) {
    throw new Error(
      `${path.relative(repoRoot, filePath)} is stale; run ` +
        "`pnpm -C app licenses:write`",
    );
  }
}

async function checkPublicFiles(aggregate) {
  const publicFiles = await expectedPublicFiles(aggregate);
  await Promise.all(
    [...publicFiles].map(([name, text]) =>
      assertCurrent(path.join(publicRoot, name), text),
    ),
  );

  const sourceNames = (await readdir(retainedLicenseRoot)).sort(compareText);
  const destinationRoot = path.join(publicRoot, "third_party_licenses");
  const destinationNames = (await readdir(destinationRoot)).sort(compareText);
  if (JSON.stringify(destinationNames) !== JSON.stringify(sourceNames)) {
    throw new Error(
      "app/public/third_party_licenses is stale; run " +
        "`pnpm -C app licenses:write`",
    );
  }
  await Promise.all(
    sourceNames.map(async (name) =>
      assertCurrent(
        path.join(destinationRoot, name),
        await readFile(path.join(retainedLicenseRoot, name), "utf8"),
      ),
    ),
  );
}

async function main() {
  const args = process.argv.slice(2);
  if (
    args.length === 2 &&
    ["--check-rust-report", "--write-rust-report"].includes(args[0])
  ) {
    const [rawReport, cargoLock, aboutConfig] = await Promise.all([
      readFile(path.resolve(args[1]), "utf8").then(JSON.parse),
      readFile(cargoLockPath, "utf8"),
      readFile(aboutConfigPath, "utf8"),
    ]);
    const expected = canonicalizeRustReport(rawReport, cargoLock, aboutConfig);
    if (args[0] === "--write-rust-report") {
      await writeIfChanged(rustReportPath, expected);
      process.stdout.write(
        `wrote ${path.relative(repoRoot, rustReportPath)}; ` +
          "run `pnpm -C app licenses:write` to refresh distributions\n",
      );
    } else {
      await assertCurrent(rustReportPath, expected);
      process.stdout.write("Rust dependency license report is current\n");
    }
    return;
  }

  if (args.length !== 1 || !["--check", "--write"].includes(args[0])) {
    throw new Error(
      "usage: generate-license-notices.mjs --check|--write|" +
        "--check-rust-report RAW_JSON|--write-rust-report RAW_JSON",
    );
  }

  const { text } = await generateLicenseNotices();
  if (args[0] === "--write") {
    await writeIfChanged(aggregatePath, text);
    await syncPublicFiles(text);
    process.stdout.write(
      "wrote THIRD_PARTY_NOTICES.txt and synchronized app/public legal files\n",
    );
    return;
  }

  await assertCurrent(aggregatePath, text);
  await checkPublicFiles(text);
  process.stdout.write("license notices are current\n");
}

if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  await main();
}
